#include<stdio.h>

int main()
{
	int a,b,c;
	a=6;
	b=2;
	
	
	c=a+b;
	printf("%d add %d gives you %d \n",a,b,c);
	

	c=a-b;
	printf("%d minus %d gives you %d \n",a,b,c);
	
	
	c=a*b;
	printf("%d multiply by %d gives you %d \n",a,b,c);
	
	
	c=a/b;
	printf("%d divided by %d gives you %d \n",a,b,c);
	

	c=a%b;
	printf("%d modulo %d gives you %d \n",a,b,c);
	
	return(0);
	
}
